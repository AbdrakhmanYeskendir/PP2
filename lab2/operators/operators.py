#Python Assignment Operators

# =
x = 5 #same as x = 5

# +=
x += 5 #same as
x = x + 3

# -=
x -= 5 #same as
x = x - 5

# *=
x *= 4 #same as
x = x *4

# /=
x /= 3 #same as
x = x / 3

# %=
x %= 5 #same as
x = x % 5

# //=
x //= 3 #same as
x = x // 3

# **=
x **= 3 #same as
x = x ** 3

# &=
x &= 3 #same as
x = x & 3   

num1 = 14
num2= 10
print (num1 & num2)

# Output:
# 10
# 14 in binary is 1110 and 10 in binary is 1010, 
# bitwise AND on these two will give us 1010 which is 10 in integers. This is how to use the & operator in Python.

