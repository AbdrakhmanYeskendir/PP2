# Python Math
# Examples of Math Operations
# Basic Math Functions

import math
print(math.sqrt(25))  # Output: 5.0
# Trigonometry

print(math.sin(math.radians(30)))  # Output: 0.5
# Ceiling and Floor

print(math.ceil(4.2))  # Output: 5
# Logarithm

print(math.log(10))  # Output: 2.3025
